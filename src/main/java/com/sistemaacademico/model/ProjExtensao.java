package com.sistemaacademico.model;

public class ProjExtensao {
    private int idProj;
    private String nome;
    private String descricao;
    private int idProf;

    // Getters e setters
    public int getIdProj() { return idProj; }
    public void setIdProj(int idProj) { this.idProj = idProj; }
    public String getNome() { return nome; }
    public void setNome(String nome) { this.nome = nome; }
    public String getDescricao() { return descricao; }
    public void setDescricao(String descricao) { this.descricao = descricao; }
    public int getIdProf() { return idProf; }
    public void setIdProf(int idProf) { this.idProf = idProf; }
}
